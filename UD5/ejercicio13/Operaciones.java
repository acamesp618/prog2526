package UD5ejercicio13;

public class Operaciones {
	
	//Con el final pueden modificar la clase pero no el método final
	 public static final double sumar(double a, double b) {
		 return a + b;
	 }
	 
	 public static double restar(double a, double b) {
		 return a - b;
	 }
	 
	 public static double multiplicar(double a, double b) {
		 return a * b;
	 }
	 
	 public static double dividir(double a, double b) {
		 return a / b;
	 }
	 
	 public final void seleccionaAlumno() {
		 // Ponemos final para que nadie pueda modificar el método
		 //Creamos código
	 }
}
